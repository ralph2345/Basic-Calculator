using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CaculatorPrivateAssembly
{
    public class BasicComputation
    {
        public static float GetSum(float a , float b)
        {
            return a + b;
        }
        public static float GetDifference(float a, float b)
        {
            return a - b;
        }
        public static float GetProduct(float a, float b)
        {
            return a * b;
        }
        public static float GetQoutient(float a, float b)
        {
            return a / b;
        }

    }
}
